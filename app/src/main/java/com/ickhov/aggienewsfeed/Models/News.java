package com.ickhov.aggienewsfeed.Models;

public class News {

    String name;
    String url;

    public News() {

    }

    public void setName(String name) {
        this.name = name;
    }

    public String getName() {
        return name;
    }

    public void setUrl(String url) {
        this.url = url;
    }

    public String getUrl() {
        return url;
    }
}
